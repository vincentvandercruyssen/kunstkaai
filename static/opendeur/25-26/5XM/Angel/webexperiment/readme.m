## Wat heb ik gemaakt?
Ik heb een interactief webexperiment gemaakt waarbij de webcam van de bezoeker wordt gebruikt als een soort spiegel. Het beeld wordt in real-time vervormd met glitch-effecten en kleurveranderingen.

## Waarom?
Ik wilde iets maken dat meteen aandacht trekt en waar mensen zichzelf in herkennen, maar dan op een vervormde en onverwachte manier.

## Bronnen / AI
Ik heb inspiratie gehaald uit online voorbeelden van canvas en glitch effects en AI gebruikt om delen van de code te genereren en aan te passen.

## Wat heb ik geleerd?
Ik heb geleerd hoe ik de webcam kan gebruiken in JavaScript en hoe ik met de canvas API beelden kan manipuleren.

## Wat zou ik anders doen?
Ik zou meer interactieve effecten toevoegen zoals reageren op beweging of geluid.